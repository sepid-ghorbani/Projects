        /// <summary>
        /// Populate business object from data reader
        /// </summary>
        /// <param name="businessObject">business object</param>
        /// <param name="dataReader">data reader</param>
        internal void PopulateBusinessObjectFromReader(ExportColors businessObject, IDataReader dataReader)
        {


				businessObject.Id = dataReader.GetInt32(dataReader.GetOrdinal(ExportColors.ExportColorsFields.Id.ToString()));

				businessObject.Name = dataReader.GetString(dataReader.GetOrdinal(ExportColors.ExportColorsFields.Name.ToString()));


        }

        /// <summary>
        /// Populate business objects from the data reader
        /// </summary>
        /// <param name="dataReader">data reader</param>
        /// <returns>list of ExportColors</returns>
        internal List<ExportColors> PopulateObjectsFromReader(IDataReader dataReader)
        {

            List<ExportColors> list = new List<ExportColors>();

            while (dataReader.Read())
            {
                ExportColors businessObject = new ExportColors();
                PopulateBusinessObjectFromReader(businessObject, dataReader);
                list.Add(businessObject);
            }
            return list;

        }

